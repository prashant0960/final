
public class swap {
	
	public static void main(String[]args) {
		
		int a=12;
		int b=24;
		int temp;
		
		System.out.println("before Swaping");
	    System.out.println("a:"+a +"b:"+b);
	    
	    temp=a;
	    a=b;
	    b=temp;
	    
	    System.out.println(" Swaping");
	    System.out.println("Value of a"+a +"Value of b"+b);
	}

}
